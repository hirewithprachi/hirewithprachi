// Helper functions to get blog posts
export const getFeaturedPosts = (limit = 3) => {
  return blogPosts.filter(post => post.featured).slice(0, limit);
};

export const getLatestPosts = (limit = 3) => {
  return blogPosts
    .sort((a, b) => new Date(b.date) - new Date(a.date))
    .slice(0, limit);
};

export const getPostsByCategory = (category, limit = 6) => {
  return blogPosts
    .filter(post => post.category === category)
    .slice(0, limit);
};

export const getPostBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

